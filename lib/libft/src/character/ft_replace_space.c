#include "libft.h"

void	ft_replace_space(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == '\t' || s[i] == '\v' || s[i] == '\f'
			|| s[i] == '\r' || s[i] == '\n')
			s[i] = ' ';
		i++;
	}
}
